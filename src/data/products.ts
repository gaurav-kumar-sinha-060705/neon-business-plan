export const generateProducts = (category: string, count: number = 15): Product[] => {
  const products: Product[] = [];
  const normalized = category.toLowerCase().replace(/\s+/g, "");
  const aliasMap: Record<string, string> = { techgadgets: "tech", homedecor: "home" };
  const key = aliasMap[normalized] || normalized;
  const names = productNames[key] || [`${category} Item`];
  const categoryMaterials = materials[key] || ["Premium"];
  const categoryTypes = types[key] || ["Standard"];
  
  // Get product images for the category
  const categoryImages = productImageMap[key] || [];
  
  for (let i = 1; i <= count; i++) {
    const nameIndex = (i - 1) % names.length;
    const materialIndex = (i - 1) % categoryMaterials.length;
    const typeIndex = (i - 1) % categoryTypes.length;
    const imageIndex = (i - 1) % categoryImages.length;
    
    // Generate varied prices based on category
    const basePrices: Record<string, number> = {
      watches: 45000,
      jewelry: 35000,
      bags: 25000,
      perfume: 8000,
      shoes: 15000,
      accessories: 5000,
      apparel: 12000,
      eyewear: 18000,
      tech: 60000,
      home: 20000
    };
    
    const basePrice = basePrices[key] || 15000;
    const price = basePrice + Math.floor(Math.random() * basePrice);
    
    // Use specific product image or fallback to unsplash
    const productImage = categoryImages[imageIndex] || `https://source.unsplash.com/800x800/?luxury,${encodeURIComponent(key)}`;
    
    products.push({
      id: `${category}-${i}`,
      name: names[nameIndex],
      price,
      image: productImage,
      category: category.charAt(0).toUpperCase() + category.slice(1),
      material: categoryMaterials[materialIndex],
      type: categoryTypes[typeIndex]
    });
  }
  
  return products;
};